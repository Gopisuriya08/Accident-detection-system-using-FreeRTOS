void gsm1_handler(){
	while(1){
		xTaskNotifyWait(0,0,eNoAction,portMAX_DELAY);
        gsmsnd("+919952641934", "Accedent detected");
       // gsmsnd("+919952641934",(char*)&GGA);
		gsmsnd("+919952641934",(char*)&link);
	}
}

void gps1_handler(){

	while(1){

          if(Wait_for("GGA")==1){
        	  Copy_upto("*", GGA);
        	  decodeGGA(GGA, &gpsdata.ggastruct);
          }
          if(Wait_for("GGA")==1){
        	  Copy_upto("*", RMC);
        	  decodeRMC(RMC, &gpsdata.rmcstruct);
          }

          snprintf(link,100,"https://www.google.com/maps?q=%.6f,%.6f",gpsdata.ggastruct.lcation.latitude,gpsdata.ggastruct.lcation.longitude);

          vTaskDelay(pdMS_TO_TICKS(1000));
	}
}
void sensor1_handler(){
     int16_t data=67;
	while(1){
		 adxl_read(0x32, 6);
	     x=(rec_data[1]<<8)|rec_data[0];
		 y=(rec_data[3]<<8)|rec_data[2];
		 z=(rec_data[5]<<8)|rec_data[4];
		//convert the value to g's uint(range +-4)
		 xg=x*0.0078; //.0078
		 yg=y*0.0078;
		 zg=z*0.0078;
		 if(y > data){
			 xTaskNotify(gsm,0,eNoAction);
		 }
		 vTaskDelay(pdMS_TO_TICKS(1000));

	}
}

